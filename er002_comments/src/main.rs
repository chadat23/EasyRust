/// Three slashes are doc comments, for doing documentation!
/// These two lines will automatically be put into the docs
fn main() {
    // This kind of comment comments out one line
    /*
    This comments out everthing between the above and below marks
    */
    print/*ln*/!("Hello, world!"); // Only part of the line was commented out!
    println!("Hello, world!");
}
